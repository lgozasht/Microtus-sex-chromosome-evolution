%rc = ("A","T","T","A","G","C","C","G");

while (<>) {
    unless (/>/) {
	if (/RC/) {
	    s/(?<=\/.)\\.+?:/&rc($&)/eg;
	}
	s/\/.+?\/(.)\\.-(\S+?-\d+).+?\\/$1($2)/g;
	s/(?<=[a-z]\().+?(?=\))/lc($&)/eg;
	s/(\S+) (\S+) (\S+)/$1$2$3/g;
    }
    print;
    
}
	
	


sub rc () {
    my $t = $_[0];
      
     $t =~ tr/ACGT/TGCA/;
    $t;
}
